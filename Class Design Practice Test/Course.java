
public class Course
{
    private String name;
    private String teacher;
    
    public Course()
    {
        this.name = name;
        this.teacher = teacher;
    }//end constructor
    
    public String getName()
    {
        return name;
    }//end getName
    
    public String getTeacher()
    {
        return teacher;
    }//end getTeacher
    
    
}//end class
