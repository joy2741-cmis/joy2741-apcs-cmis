public class MySong
{
    private String name;
    private int rating;

    //constructor

    public MySong( String name, int rating )
    {
        this.name = name;
        this.rating = rating;
    }//end constructor

    //getter/setter

    public String getName()
    {
        return name;
    }//end getName

    public int getRating()
    {
        return rating;
    }//end getNum
}//end class